# Size bracket multipliers: (max_ratio, multiplier)
# ratio = caught_size / avg_size
# Applied from smallest to largest; first matching bracket wins (searching upward)
SIZE_BRACKETS = [
    (0.25,  1.75),   # < 0.25x avg  → ×1.75
    (0.50,  0.60),   # 0.25–0.5x    → ×0.60
    (1.00,  0.80),   # 0.5–1.0x     → ×0.80
    (1.50,  1.00),   # 1.0–1.5x     → ×1.00
    (2.00,  1.50),   # 1.5–2.0x     → ×1.50
    (3.00,  2.50),   # 2.0–3.0x     → ×2.50
    (float("inf"), 4.25),  # ≥ 3.0x → ×4.25
]

SIZE_LABELS = [
    (0.25,  "Microscopic"),
    (0.50,  "Tiny"),
    (1.00,  "Small"),
    (1.50,  "Average"),
    (2.00,  "Large"),
    (3.00,  "Massive"),
    (float("inf"), "Colossal"),
]


def get_fish(fish_id: str) -> dict | None:
    return FISH_BY_ID.get(fish_id)


def get_fish_by_biome(biome_id: str, include_event: bool = False) -> list[dict]:
    """Returns all fish available in a given biome."""
    return [
        f for f in FISH
        if biome_id in f["biomes"]
        and (include_event or not f["is_event"])
    ]


def get_event_fish(event_id: str) -> dict | None:
    """Returns the fish dict for a given event_id."""
    for f in FISH:
        if f.get("event_id") == event_id:
            return f
    return None


def get_size_multiplier(size: float, avg_size: float) -> tuple[float, str]:
    """Returns (multiplier, label) for a given size."""
    ratio = size / avg_size
    for max_ratio, mult in SIZE_BRACKETS:
        if ratio < max_ratio:
            for max_r2, label in SIZE_LABELS:
                if ratio < max_r2:
                    return mult, label
            return mult, "Colossal"
    return 4.25, "Colossal"
